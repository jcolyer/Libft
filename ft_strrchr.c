char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*ptr;
	while (s[i])
	{
		if (s[i] == c)
			ptr = (char *)(s + i);
		i++;
	}
	if (s[i] == c)
		ptr = (char *)(s + i);
	return (ptr);
}
